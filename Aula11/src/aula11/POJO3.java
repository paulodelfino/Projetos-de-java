
package aula11;


public class POJO3 {
    
    private String ceu = "Estrelado";
    private String clima = "Temperado";
    private String mar = "Agitado";
    
    public POJO3 (){
        
    }

    public String getCeu() {
        return ceu;
    }

    public void setCeu(String ceu) {
        this.ceu = ceu;
    }

    public String getClima() {
        return clima;
    }

    public void setClima(String clima) {
        this.clima = clima;
    }

    public String getMar() {
        return mar;
    }

    public void setMar(String mar) {
        this.mar = mar;
    }
    
}
