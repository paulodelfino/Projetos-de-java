    private void MeuMetodo() {
        DefaultTableModel dtb = (DefaultTableModel)jTable1.getModel();
        
        for (int i = 0; i < 10; i++) {
            Entidade ent = new Entidade();
            Calendar cal = Calendar.getInstance();
            cal.add(Calendar.DAY_OF_YEAR,i);
            ent.setId(i);
            ent.setNome("Pipoca nÂ°: "+i);
            ent.setIdade(i+20);
            ent.setDn(cal.getTime());
            
            lista.add(ent);
        }
        for (Entidade et:lista) {
                dtb.addRow(new Object[] {et.getId(),et.getNome(),et.getIdade(),et.getDn()});
            }
    }
