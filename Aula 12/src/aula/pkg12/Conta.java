
package aula.pkg12;


public class Conta {
    
    public int soma (int v1, int v2){
         int total= v1+v2;
         
         return total;
        
    }
    
    public int subtracao (int v1, int v2){
         int total= v1-v2;
         
         return total;
}
    
    public int multiplicacao (int v1, int v2){
         int total= v1*v2;
         
         return total;
}
    public double divisao (double v1, double v2){
         double total= v1/v2;
         
         return total;
    
    
}
}